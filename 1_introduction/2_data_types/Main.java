class Main {
	public static void main(String[] args) {
		// Boolean
		boolean flag = true;
		System.out.println(flag);
		// Byte 8 bit
		byte range; range = 124;
		System.out.println(range);
		// Short 16 bit
		short temperature = -200;
		System.out.println(temperature);
		// Int 32 bit
		int int_range = -425000000;
		System.out.println(int_range);
		// Long 64 bit
		long long_range = -42332200000L;
		System.out.println(long_range);
		
		// Float single precision 32
		float float_number = -23.12f;
		System.out.println(float_number);
		// Double double precision 64
		double double_number = -23.12;
		System.out.println(double_number);

		// Char 16 bit unicode character
		char letter = '\u0051';
		System.out.println(letter);
		char letter_1 = '9';
		System.out.println(letter_1);
		char letter_2 = 65;
		System.out.println(letter_2);

		// String is Object
		String myString = "Hi Mom!";
		System.out.println(myString);
	}
}
