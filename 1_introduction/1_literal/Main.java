class Main {
	public static void main(String[] args) {
		// Boolean literals
		boolean flag = true;
		boolean flag = false;

		// Integers literals
		int binaryNumber = 0b10010;
		int octalNumber = 027;
		int decimalNumber = 34;
		int hexadecimalNumber = 0x2f;

		// Floating point literals
		float floatNumber = 3.4f;
		double doubleNumber = 3.4;
		double myScientificDouble = 3.445e2; // 3.445*10^2
		
		// Character literals
		char letter = 'L';
		
		// String literals
		String myString = "Hello World";
	}
}
